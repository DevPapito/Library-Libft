static	int	ft_isupper(int c)
{
	if (!(c >= 'A' && c <= 'Z'))
		return (0);
	return (1);
}

int	ft_toupper(int c)
{
	if (ft_isupper(c) == 1)
		return (c);
	c -= 32;
	return (c);
}
